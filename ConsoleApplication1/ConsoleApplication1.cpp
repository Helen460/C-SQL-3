// 1. Реалізувати калькулятор. Вводяться 2 будь-яких дійсних числа в змінні a і b. Необхідно вивести на екран меню з пунктами:
//a + b
//a – b
//a * b
//a / b
//a % b
//При виборі відповідного пункту меню відбувається виведення результату обчислення(повинна бути перевірка ділення на 0!)
//Спробувати використати як драбинку іфів (1.1) та  свіч (1.2).
//1.1 Драбинка if-else
#include <iostream>
#include <windows.h>
using namespace std;

int main() {
    SetConsoleOutputCP(1251);

    double a;
    double b;
    int choice;

    cout << "Введіть дїйсне число, a: ";
    cin >> a;
    cout << "Введіть дійсне число, b: ";
    cin >> b;

    cout << "Оберіть операцію:\n";
    cout << "1) a + b\n";
    cout << "2) a - b\n";
    cout << "3) a * b\n";
    cout << "4) a / b\n";
    cout << "5) a % b\n";

    cin >> choice;

    if (choice == 1) {
        cout << "Результат: " << a + b << "\n";
    }
    else if (choice == 2) {
        cout << "Результат: " << a - b << "\n";
    }
    else if (choice == 3) {
        cout << "Результат: " << a * b << "\n";
    }
    else if (choice == 4) {
        if (b == 0) {
            cout << "Помилка! Ділення на 0 неможливе.\n";
        }
        else {
            cout << "Результат: " << a / b << "\n";
        }
    }
    else if (choice == 5) {

        cout << "Результат: " << (int)a % (int)b << "\n";
    }
    else {
        cout << "Невірний вибір.\n";
    }
}

