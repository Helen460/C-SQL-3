#include <iostream>
#include <windows.h>
using namespace std;

int main() {
    SetConsoleOutputCP(1251);

    double a;
    double b;
    int choice;

    cout << "Введіть дїйсне число, a: ";
    cin >> a;
    cout << "Введіть дійсне число, b: ";
    cin >> b;

    cout << "Оберіть операцію:\n";
    cout << "1) a + b\n";
    cout << "2) a - b\n";
    cout << "3) a * b\n";
    cout << "4) a / b\n";
    cout << "5) a % b\n";

    cin >> choice;

    switch (choice) {
    case 1:
        cout << "Результат: " << a + b << "\n";
        break;
    case 2:
        cout << "Результат: " << a - b << "\n";
        break;
    case 3:
        cout << "Результат: " << a * b << "\n";
        break;
    case 4:
        if (b == 0) {
            cout << "Помилка! Ділення на 0 неможливе.\n";
        }
        else {
            cout << "Результат: " << a / b << "\n";
        }
        break;
    case 5:
        cout << "Результат: " << (int)a % (int)b << "\n";
        break;
    default:
        cout << "Невірний вибір.\n";
        break;
    }
}

